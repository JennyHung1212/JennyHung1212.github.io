        <br />
        <br />We arrived at a bus terminal called Larkin Sentral at Johor Bahru at noon. The driver shout out "luggage" and asked everyone to get off the bus without any further instruction. I didn't think much and follow the passengers in front of me into the terminal, at that time, I was still not sure whether it's a terminal or a checkpoint. Later I found out that, if my destination was Singapore, I should follow the driver to another bus, not into the terminal. I was so shocked and nervous. I rushed out to the terminal immediately but the original bus was already gone, and I had no idea which bus to transfer to.
